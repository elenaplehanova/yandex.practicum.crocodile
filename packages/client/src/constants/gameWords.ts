// Функция для получения случайного слова
export const getRandomWord = (): string => {
  const randomIndex = Math.floor(Math.random() * GAME_WORDS.length)
  return GAME_WORDS[randomIndex]
}

// Функция для получения случайного слова, отличного от текущего
export const getNextWord = (currentWord: string): string => {
  let nextWord = getRandomWord()
  while (nextWord === currentWord && GAME_WORDS.length > 1) {
    nextWord = getRandomWord()
  }
  return nextWord
}
